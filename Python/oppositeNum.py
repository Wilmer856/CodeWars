#Very simple, given a number (integer / decimal / both depending on the language),
#find its opposite (additive inverse).

# Examples:
# opposite(1): -1
# opposite(14): -14
# opposite(-34): 34

# returns its opposite (additive inverse) by multiplying it by -1.
# This is because the opposite of a number on the number line is obtained by negating it.
def opposite(number):
    return number*-1
